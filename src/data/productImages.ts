// Helper function to get product images
export function getProductImages(productSlug: string) {
  const entry = (productImages as any)[productSlug];
  if (!entry) {
    return {
      image: '/api/placeholder/400/300',
      gallery: [
        '/api/placeholder/400/300',
        '/api/placeholder/400/300',
        '/api/placeholder/400/300'
      ]
    };
  }
  
  if (typeof entry === 'string') {
    // Category image
    return {
      image: entry,
      gallery: [entry, entry, entry]
    };
  }
  
  // Product with gallery
  return entry;
}

// Get category image helper
export function getCategoryImage(categorySlug: string): string {
  return (productImages as any)[categorySlug] || '/api/placeholder/400/300';
}
